package Clases;

public class Voto {
    
   private String nombreUsuario;   
   private Boolean votoNegativo;
   private Boolean votoPositivo;

    public String getNombreUsuario() {
        return nombreUsuario;
    }

    public Boolean getVotoNegativo() {
        return votoNegativo;
    }

    public Boolean getVotoPositivo() {
        return votoPositivo;
    }

    public void setNombreUsuario(String nombreUsuario) {
        this.nombreUsuario = nombreUsuario;
    }

    public void setVotoNegativo(Boolean votoNegativo) {
        this.votoNegativo = votoNegativo;
    }

    public void setVotoPositivo(Boolean votoPositivo) {
        this.votoPositivo = votoPositivo;
    }

    
    
    
    
    
    
}
